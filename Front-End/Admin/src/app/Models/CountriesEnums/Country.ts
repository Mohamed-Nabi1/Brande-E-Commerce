export function getCountryNameById(id: number): string {
  return Country[id] || `Country ID: ${id}`;
}

export function getCountriesList(): {id: number, name: string}[] {
  return Object.keys(Country)
    .filter(key => isNaN(Number(key)))
    .map(name => ({
      id: Country[name as keyof typeof Country],
      name: name
    }))
    .sort((a, b) => a.name.localeCompare(b.name));
}